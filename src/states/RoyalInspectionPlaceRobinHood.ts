  private updateInterfaceInitialStep() {
    this.game.clearPossible();

    this.game.clientUpdatePageTitle({
      text: _('${you} must select a Forest to place Robin Hood in'),
      args: {
        you: '${you}',
      },
    });

    Object.values(this.args._private.spaces).forEach((space) => {
      this.game.setSpaceSelectable({
        id: space.id,
        callback: () => {
          this.updateInterfaceConfirm({ space });
        },
      });
    });

    this.game.addPassButton({
      optionalAction: this.args.optionalAction,
    });
    this.game.addUndoButtons(this.args);
  }

  private updateInterfaceConfirm({
    space,
    placeRobinHood = false,
  }: {
    space: GestSpace;
    placeRobinHood?: boolean;
  }) {
    this.game.clearPossible();

    this.game.clientUpdatePageTitle({
      text: _('Place Robin Hood in ${spaceName}?'),
      args: {
        spaceName: _(space.name),
      },
    });

    const callback = () => {
      this.game.clearPossible();
      this.game.takeAction({
        action: 'actRoyalInspectionPlaceRobinHood',
        args: {
          spaceId: space.id,
        },
      });
    };

    if (
      this.game.settings.get({
        id: PREF_CONFIRM_END_OF_TURN_AND_PLAYER_SWITCH_ONLY,
      }) === PREF_ENABLED
    ) {
      callback();
    } else {
      this.game.addConfirmButton({
        callback,
      });
    }

    this.game.addCancelButton();
  }


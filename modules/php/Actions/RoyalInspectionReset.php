  public function stRoyalInspectionReset()
  {
    Cards::moveAllInLocation(TRAVELLERS_DISCARD, TRAVELLERS_DECK);
    Cards::shuffle(TRAVELLERS_DECK);

    Notifications::returnTravellersDiscardToMainDeck();

    $markers = Markers::get([ROBIN_HOOD_ELIGIBILITY_MARKER, SHERIFF_ELIGIBILITY_MARKER]);

    $markers[ROBIN_HOOD_ELIGIBILITY_MARKER]->setLocation(Locations::initiativeTrack(FIRST_ELIGIBLE));
    $markers[SHERIFF_ELIGIBILITY_MARKER]->setLocation(Locations::initiativeTrack(SECOND_ELIGIBLE));

    Notifications::firstEligible($markers[ROBIN_HOOD_ELIGIBILITY_MARKER]);
    Notifications::secondEligible($markers[SHERIFF_ELIGIBILITY_MARKER]);

    $this->resolveAction(['automatic' => true]);
  }



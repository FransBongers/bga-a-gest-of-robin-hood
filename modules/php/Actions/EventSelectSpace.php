  public function stEventSelectSpace()
  {
    $info = $this->ctx->getInfo();
    $cardId = $info['cardId'];
    $effect = $info['effect'];
    $player = self::getPlayer();

    $isResolved = Cards::get($cardId)->resolveEffectAutomatically($player, $effect, $this->ctx);

    if ($isResolved) {
      $this->resolveAction(['automatic' => true]);
    }
  }


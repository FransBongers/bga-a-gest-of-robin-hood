  public function stPutTravellerInDiscardPile()
  {

    $cardId = $this->ctx->getInfo()['cardId'];

    $card = Cards::get($cardId);
    $card->setLocation(TRAVELLERS_DISCARD);

    Notifications::putTravellerInDiscardPile(self::getPlayer(), $card);
    $this->resolveAction(['automatic' => true]);
  }


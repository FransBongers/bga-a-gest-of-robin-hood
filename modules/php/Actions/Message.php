  public function stMessage()
  {
    $info = $this->ctx->getInfo();
    $messageType = $info['messageType'];
    $args = [
      'player' => self::getPlayer()
    ];
    $text = '';

    switch ($messageType) {
      case MESSAGE_CANNOT_PERFORM_EVENT:
        $text = clienttranslate('${player_name} does not meet the requirements to perform the Event');
        break;
      default:
    }

    Notifications::message($text, $args);
    $this->resolveAction(['automatic' => true]);
  }

